import math

def areaOfRectangle(baseValue, heightValue):
    #CITATION - URL: https://www.geeksforgeeks.org/area-of-rectangle/
    #CITATION - Date Written/Posted:  Last Updated : 15 Oct, 2024 
    return baseValue * heightValue

def rectanglePerimeter(lengthValue, widthValue):
    #CITATION - URL: https://www.geeksforgeeks.org/perimeter-of-rectangle/
    #CITATION - Date Written/Posted:   Last Updated : 13 Jan, 2025 
    return 2 * (lengthValue + widthValue)

def areaOfCircle(radiusValue):
    #CITATION - URL: https://www.geeksforgeeks.org/area-of-a-circle/
    #CITATION - Date Written/Posted: Last Updated : 14 Jan, 2025 
    num = math.pi
    return num * (radiusValue ** 2)

def circleCircumference(radiusValue):
    #CITATION - URL: https://tutors.com/lesson/what-is-circumference-definition-equation
    #CITATION - Date Written/Posted: January 26, 2023
    #CITATION - Author: Malcolm McKinsey
    num = math.pi
    return 2 * num * radiusValue